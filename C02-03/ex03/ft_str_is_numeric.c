int	ft_str_is_numeric(char *str)
{
	int is_valid;

	is_valid = 1;
	if (*str != '\0')
	{
		while (*str != '\0' && is_valid)
		{
			is_valid = *str >= '0' && *str <= '9';
			str++;
		}
	}
	return (is_valid);
}
