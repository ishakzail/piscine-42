#include "ft_list.h"

void	ft_list_reverse(t_list **begin_list)
{
	t_list *next;
	t_list *current;
	t_list *previous;

	current = *begin_list;
	previous = NULL;
	while (current)
	{
		next = current->next;
		current->next = previous;
		previous = current;
		current = next;
	}
	*begin_list = previous;
}
