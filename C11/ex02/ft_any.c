int	ft_any(char **tab, int (*f)(char*))
{
	int i;
	int tab_len;

	i = -1;
	tab_len = 0;
	while (tab[tab_len])
		tab_len++;
	while (++i < tab_len)
	{
		if ((*f)(tab[i]) != 0)
			return (1);
	}
	return (0);
}
